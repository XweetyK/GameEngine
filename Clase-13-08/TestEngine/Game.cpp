bool Game::onUpdate(double deltaTime) {
	std::cout << "game::onUpdate()";
	_i++;
	std::cout <<_i<< endl;

	_trgRot += deltaTime*_trgVel;
	_sqrRot += deltaTime*_sqrVel;
	_sprtMov = deltaTime*_sprtVel;

	if (input(UP_INPUT)) {
		cout << "up" << endl;
		_tilemap->SetPos(_tilemap->GetPos(1), _tilemap->GetPos(2) - _sprtMov, 0.0f);
	}
	if (input(DOWN_INPUT)) {
		_tilemap->SetPos(_tilemap->GetPos(1), _tilemap->GetPos(2) + _sprtMov, 0.0f);
	}
	if (input(LEFT_INPUT)) {
		_tilemap->SetPos(_tilemap->GetPos(1) + _sprtMov, _tilemap->GetPos(2), 0.0f);
	}
	if (input(RIGHT_INPUT)) {
		_tilemap->SetPos(_tilemap->GetPos(1) - _sprtMov, _tilemap->GetPos(2), 0.0f);
	}
	
	_triangle->SetRot(0.0f, 0.0f, _trgRot);
	_circle->SetRot(0.0f, _trgRot*2, 0.0f);
	//_square->SetRot(_sqrRot,0.0f, _sqrRot);
	//_square->SetPos(_square->GetPosX()+_sprtMov, 9.0f, 0.0f);
	if (_collisionManager->IsColliding()) {
		if (!_activated) {
			_cris->SetAnimatorFrames(_damageFrames, 2, 10);
			_cris2->SetAnimatorFrames(_punchFrames, 5, 10);
			_activated = true;
		}
	}
	else { _activated = false; }

	_cris->UpdateAvatar(deltaTime,true);
	_cris2->UpdateAvatar(deltaTime, true);

	cout << _tilemap->GetPos(1) << " , " << _tilemap->GetPos(2) << endl;
	_ambulancia->SetPos(_ambulancia->GetPosX() + _sprtMov*2.5f, _ambulancia->GetPosY(), _ambulancia->GetPosZ());
	_cris2->SetSpritePos(_cris2->GetSprite()->GetPosX() + _sprtMov, _cris->GetSprite()->GetPosY(), _cris->GetSprite()->GetPosZ());
	
	_collisionManager->UpdateCollider();
	
	
	if (_i > 5) {
		return false;
	}
	return true;
}
